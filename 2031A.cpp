#include <bits/stdc++.h>
using namespace std;
#define int long long int
#define vi vector<int>
#define read(a) for(auto &i: a) cin>>i
#define mod 1000000007
#define nline '\n'
signed main() {
ios_base::sync_with_stdio(false);
cin.tie(NULL);
int t=1;
cin>>t;
while(t--){
int n; cin>>n;
vi arr(n); read(arr);
int ans=1e9;
for(int i=0; i<n; i++){
    vi arr1=arr;
    for(int j=i+1; j<n; j++){
        if(arr1[j]<arr1[j-1]) arr1[j]=arr1[j-1];
    }
    for(int j=i-1; j>=0; j--){
        if(arr1[j]>arr1[j+1]) arr1[j]=arr1[j+1];
    }
    int curr=0;
for(int j=0; j<n; j++) curr +=(arr[j]!= arr1[j]);
ans=min(ans,curr);
} 
cout<<ans<<nline;
}
return 0;}